    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        if (customerIdTextField_search.getText().length() > 0) {
            int customerToSearch = Integer.parseInt(customerIdTextField_search.getText());
            errorLabel_cutomerId_search.setText("");
            ArrayList<CustomerData> customerList = customerMethod.retriveAllCustomer();
            for (Object customer : customerList) {
                CustomerData data = (CustomerData) customer;
                if (customerToSearch == data.getCustomerId()) {
                    searchResultTextArea.setText("Customer Id:" + data.getCustomerId() + "\nName:" + data.getName() + "\nPhone:" + data.getPhone() + "\nPostal Code:" + data.getPostalCode() + "\nEmail:" + data.getEmail());
                    nameTextField_edit.setText(data.getName());
                    emailTextField_edit.setText(data.getEmail());
                    phoneTextField_edit.setText(data.getPhone());
                    postalCodeTextField_edit.setText(data.getPostalCode());
                    customerData_index.setText(Integer.toString(customerList.indexOf(data)));
                    errorLabel_cutomerId_search.setText("");
                    customerData_index.setVisible(false);
                    edit_success.setText("");

                    editCustomerLink.setVisible(true);
                    break;
                } else {
                    editCustomerLink.setVisible(false);

                    errorLabel_cutomerId_search.setText("Customer id is not found!");
                    searchResultTextArea.setText("");
                }
            }
        } else {
            editCustomerLink.setVisible(false);
            
            errorLabel_cutomerId_search.setText("Please Enter Valid customer id");
            searchResultTextArea.setText("");
        }
    }//GEN-LAST:event_searchButtonActionPerformed


    private void only10DigitsAllowed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_only10DigitsAllowed
        char inputChar = evt.getKeyChar();
        if (!((inputChar >= evt.VK_0 && (char) inputChar <= evt.VK_9) || inputChar == evt.VK_BACK_SPACE)) {
            evt.consume();

            if (evt.getComponent().getName().equals("add")) {
                errorLabel_phone_add.setText("Only digits are allowed.");
            } else if (evt.getComponent().getName().equals("edit")) {
                errorLabel_phone_edit.setText("Only digits are allowed.");
            }
        } else {
            if (evt.getComponent().getName().equals("add")) {
                if (!(phoneTextField_add.getText().length() < 10)) {
                    evt.consume();
                } else {
                    errorLabel_phone_add.setText(" ");
                }
            } else if (evt.getComponent().getName().equals("edit")) {
                if (!(phoneTextField_edit.getText().length() < 10)) {
                    evt.consume();
                } else {
                    errorLabel_phone_edit.setText(" ");
                }
            }
        }
    }//GEN-LAST:event_only10DigitsAllowed


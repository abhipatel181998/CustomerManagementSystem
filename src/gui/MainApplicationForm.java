    private void addCustomerButton_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCustomerButton_addActionPerformed

        String name = nameTextField_add.getText();
        String phone = phoneTextField_add.getText();
        String email = emailTextField_add.getText();
        String postalCode = postalCodeTextField_add.getText();

        isNameFieldVaild = Boolean.parseBoolean((String) (validationMethod.validateNameField(name)).get("isValid"));
        isPhoneFieldVaild = Boolean.parseBoolean((String) (validationMethod.validatePhoneField(phone)).get("isValid"));
        isEmailFieldVaild = Boolean.parseBoolean((String) (validationMethod.validateEmailField(email)).get("isValid"));
        isPostalCodeFieldVaild = Boolean.parseBoolean((String) (validationMethod.validatePostalCodeField(postalCode)).get("isValid"));

        if (isNameFieldVaild && isPhoneFieldVaild && isEmailFieldVaild && isPostalCodeFieldVaild) {
            int customerId = customerMethod.lastCustomerId();

            CustomerData customer = new CustomerData(customerId, name, phone, email, postalCode);

            customerMethod.addCustomer(customer);

            addResultLabel.setForeground(Color.GREEN);
            addResultLabel.setText("Customer added successfully.");
        } else if (name.equals("") || phone.equals("") || email.equals("") || postalCode.equals("")) {
            addResultLabel.setForeground(Color.RED);
            addResultLabel.setText("Please make sure to fill all the text field.");
        } else {
            addResultLabel.setText("");
        }

    }//GEN-LAST:event_addCustomerButton_addActionPerformed

